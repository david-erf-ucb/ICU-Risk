# 3) Impute NaNs with TRAIN column means
#    RF cannot handle NaN natively (unlike LightGBM)
# ============================================================
def fit_imputer(X_train):
    col_means = np.nanmean(X_train, axis=0)
    col_means = np.where(np.isfinite(col_means), col_means, 0.0)
    return col_means

def apply_imputer(X, col_means):
    X_imp = X.copy()
    nan_mask = np.isnan(X_imp)
    X_imp[nan_mask] = np.take(col_means, np.where(nan_mask)[1])
    return X_imp

