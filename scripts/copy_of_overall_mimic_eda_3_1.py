# 8) Top / bottom "performing" features (Permutation importance on PR-AUC)
#    We shuffle feature j across stays for all timesteps; PR-AUC drop = importance
# ============================================================
def permutation_importance_pr_auc(model, X, y, feature_names, n_stays=2000, repeats=1, seed=42):
    rng = np.random.default_rng(seed)

    n = X.shape[0]
    idx = np.arange(n)
    if n_stays is not None and n_stays < n:
        idx = rng.choice(idx, size=n_stays, replace=False)

    Xs = X[idx].copy()
    ys = y[idx].copy()

    base_p = model.predict(Xs, batch_size=512, verbose=0).ravel()
    base_pr = average_precision_score(ys, base_p)

    drops = np.zeros(len(feature_names), dtype=np.float32)

    for j in range(len(feature_names)):
        drop_rep = []
        for _ in range(repeats):
            Xp = Xs.copy()
            perm = rng.permutation(Xp.shape[0])
            Xp[:, :, j] = Xp[perm, :, j]
            p = model.predict(Xp, batch_size=512, verbose=0).ravel()
            pr = average_precision_score(ys, p)
            drop_rep.append(base_pr - pr)
        drops[j] = float(np.mean(drop_rep))

    imp = pd.DataFrame({"feature": feature_names, "pr_auc_drop": drops})
    imp = imp.sort_values("pr_auc_drop", ascending=False).reset_index(drop=True)
    return base_pr, imp

