    print(f"\n=== {name} (thr={threshold:.4f}) ===")
    print(f"PR-AUC : {pr:.4f}")
    print(f"ROC-AUC: {roc:.4f}")
    print(f"Confusion matrix:\n{cm}")
    print("\nReport:")
    print(classification_report(y, yhat, digits=4))
    return p, yhat, cm, pr, roc

