# 5) KerasTuner model builder (regularized search space)
#    We constrain capacity a bit to reduce overfitting.
# ============================================================
def build_model(hp: kt.HyperParameters):
    n_features = X_train.shape[-1]

    units1 = hp.Int("units1", 32, 128, step=32)
    units2 = hp.Int("units2", 0, 64, step=32)  # 0 => single-layer LSTM
    dropout = hp.Float("dropout", 0.1, 0.5, step=0.1)
    rec_dropout = hp.Float("recurrent_dropout", 0.0, 0.3, step=0.1)
    dense_units = hp.Int("dense", 16, 64, step=16)
    lr = hp.Choice("lr", [5e-4, 2e-4, 1e-4])  # slightly lower than 1e-3 tends to generalize better
    l2 = hp.Choice("l2", [0.0, 1e-5, 1e-4])

    reg = keras.regularizers.l2(l2) if l2 and l2 > 0 else None

    inputs = keras.Input(shape=(MAX_T, n_features))
    x = layers.Masking(mask_value=0.0)(inputs)

    x = layers.LSTM(
        units1,
        return_sequences=(units2 > 0),
        dropout=dropout,
        recurrent_dropout=rec_dropout
    )(x)

    if units2 > 0:
        x = layers.LSTM(
            units2,
            return_sequences=False,
            dropout=dropout,
            recurrent_dropout=rec_dropout
        )(x)

    x = layers.Dense(dense_units, activation="relu", kernel_regularizer=reg)(x)
    x = layers.Dropout(dropout)(x)
    outputs = layers.Dense(1, activation="sigmoid")(x)

    model = keras.Model(inputs, outputs)
    model.compile(
        optimizer=keras.optimizers.Adam(learning_rate=lr),
        loss="binary_crossentropy",
        metrics=[
            keras.metrics.BinaryAccuracy(name="accuracy"),
            keras.metrics.AUC(curve="PR", name="pr_auc"),
        ],
    )
    return model

