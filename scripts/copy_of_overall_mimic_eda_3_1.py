def collapse_to_4h_bins_safe(df, label_col=None, max_hours=24, verbose=True):
    df = df[df["hours_in"].between(0, max_hours - 1)].copy()
    df["tbin"] = (df["hours_in"] // 4).astype(np.int16)

    id_cols = ["subject_id", "hadm_id", "stay_id", "tbin"]
    exclude = set(id_cols + ["hours_in"] + ([label_col] if label_col else []))

    feat_cols = [c for c in df.columns if c not in exclude]

    # Split numeric vs non-numeric
    numeric_feat_cols = [c for c in feat_cols if pd.api.types.is_numeric_dtype(df[c])]
    non_numeric_feat_cols = [c for c in feat_cols if c not in numeric_feat_cols]

    if verbose and len(non_numeric_feat_cols) > 0:
        print(f"[collapse_to_4h_bins_safe] Skipping {len(non_numeric_feat_cols)} "
              f"non-numeric feature cols (example): {non_numeric_feat_cols[:10]}")

    # Identify tuple-based mean/count columns
    mean_cols  = [c for c in numeric_feat_cols if isinstance(c, tuple) and len(c) == 2 and c[1] == "mean"]
    count_cols = [c for c in numeric_feat_cols if isinstance(c, tuple) and len(c) == 2 and c[1] == "count"]
    other_cols = [c for c in numeric_feat_cols if c not in mean_cols and c not in count_cols]

    # Detect binary among other numeric cols
    binary_cols, cont_cols = [], []
    for c in other_cols:
        s = df[c]
        vals = pd.unique(s.dropna())
        if len(vals) > 0 and set(vals).issubset({0, 1, 0.0, 1.0}):
            binary_cols.append(c)
        else:
            cont_cols.append(c)

    agg = {}
    for c in mean_cols:   agg[c] = "mean"
    for c in count_cols:  agg[c] = "sum"
    for c in binary_cols: agg[c] = "max"
    for c in cont_cols:   agg[c] = "mean"
    if label_col:         agg[label_col] = "max"

    binned = (
        df.groupby(id_cols, sort=False)
          .agg(agg)
          .reset_index()
          .sort_values(["stay_id", "tbin"])
          .reset_index(drop=True)
    )

    return binned

