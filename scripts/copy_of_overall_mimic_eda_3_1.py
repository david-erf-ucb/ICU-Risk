#    Features per original col: mean, std, min, max, last, count_observed, miss_rate
#    Final shape: (N_stays, F * 7)
# ============================================================
def flatten_sequences(df: pd.DataFrame, feature_cols: list, label_col: str, max_t: int):
    y_stay   = df.groupby("stay_id")[label_col].max()
    df_sorted = df.sort_values(["stay_id", TIME_COL])

    rows, y_list, stay_list = [], [], []
    flat_cols = []
    for feat in feature_cols:
        flat_cols += [
            f"{feat}__mean", f"{feat}__std", f"{feat}__min",
            f"{feat}__max", f"{feat}__last", f"{feat}__count",
            f"{feat}__miss_rate"
        ]

    for stay_id, g in df_sorted.groupby("stay_id", sort=False):
        X = np.full((max_t, len(feature_cols)), np.nan, dtype=np.float32)

        tb   = np.clip(g[TIME_COL].astype(int).values, 0, max_t - 1)
        vals = g[feature_cols].to_numpy(dtype=np.float32)

        for t in np.unique(tb):
            rr   = vals[tb == t]
            X[t] = rr[0] if rr.shape[0] == 1 else np.nanmean(rr, axis=0)

        row = []
        for j in range(len(feature_cols)):
            col_vals = X[:, j]
            observed  = col_vals[~np.isnan(col_vals)]
            miss_rate = float(np.isnan(col_vals).mean())

            if len(observed) == 0:
                row += [np.nan, np.nan, np.nan, np.nan, np.nan, 0.0, miss_rate]
            else:
                row += [
                    float(np.nanmean(col_vals)),
                    float(np.nanstd(col_vals)) if len(observed) > 1 else 0.0,
                    float(np.nanmin(col_vals)),
                    float(np.nanmax(col_vals)),
                    float(observed[-1]),
                    float(len(observed)),
                    miss_rate
                ]

        rows.append(row)
        y_list.append(float(y_stay.loc[stay_id]))
        stay_list.append(stay_id)

    X_flat = np.array(rows, dtype=np.float32)
    y      = np.array(y_list, dtype=np.float32)
    stays  = np.array(stay_list)
    return X_flat, y, stays, flat_cols

