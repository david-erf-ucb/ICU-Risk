# 1) Leakage-safe feature selection (numeric only)
# ============================================================
def is_suspicious(col_name: str, keywords: list) -> bool:
    s = str(col_name).lower()
    return any(k in s for k in keywords)

def get_numeric_feature_cols_leakage_safe(
    df, label_col, id_cols, leakage_cols, suspicious_keywords, verbose=True
):
    exclude = set(id_cols + [label_col] + list(leakage_cols))
    candidates = [c for c in df.columns if c not in exclude]
    numeric_candidates = [c for c in candidates if pd.api.types.is_numeric_dtype(df[c])]
    suspicious = [c for c in numeric_candidates if is_suspicious(c, suspicious_keywords)]
    safe = [c for c in numeric_candidates if c not in suspicious]

    if verbose:
        print(f"Candidate numeric features: {len(numeric_candidates)}")
        if suspicious:
            print(f"Dropped suspicious keyword features: {len(suspicious)} (example): {suspicious[:15]}")
        present_leak = [c for c in leakage_cols if c in df.columns]
        if present_leak:
            print(f"Leakage cols excluded (present): {len(present_leak)} (example): {present_leak[:15]}")
        if label_col in safe:
            raise ValueError("Label column leaked into features!")
    return safe

feature_cols = get_numeric_feature_cols_leakage_safe(
    train_df, LABEL_COL, ID_COLS, leakage_cols, suspicious_keywords, verbose=True
)

def drop_bad_cols(train_df, cols):
    good, dropped_all_nan, dropped_const = [], [], []
    for c in cols:
        s = train_df[c]
        if s.notna().sum() == 0:
            dropped_all_nan.append(c)
            continue
        if s.dropna().nunique() <= 1:
            dropped_const.append(c)
            continue
        good.append(c)
    return good, dropped_all_nan, dropped_const

