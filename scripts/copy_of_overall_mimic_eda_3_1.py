# Standardize values only (flags untouched)
def fit_standardizer(X_train_aug, F):
    vals = X_train_aug[..., :F].reshape(-1, F)
    mu = vals.mean(axis=0).astype(np.float32)
    sd = vals.std(axis=0).astype(np.float32)
    sd = np.where(sd > 1e-6, sd, 1.0).astype(np.float32)
    return mu, sd

