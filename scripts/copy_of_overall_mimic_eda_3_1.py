# 7) Threshold tuning on VALIDATION (best strategy)
#    - Default: choose threshold that maximizes F1 on validation
#    - Alternative: choose threshold meeting TARGET_RECALL
# ============================================================
def tune_threshold(y_true, p, mode="max_f1", target_recall=0.85):
    precision, recall, thr = precision_recall_curve(y_true, p)
    # precision_recall_curve returns thr of length n-1; align carefully
    if mode == "target_recall":
        # pick highest precision among thresholds that achieve recall >= target
        valid = np.where(recall[:-1] >= target_recall)[0]
        if len(valid) == 0:
            # fallback to max F1
            mode = "max_f1"
        else:
            best_i = valid[np.argmax(precision[:-1][valid])]
            return float(thr[best_i]), float(precision[:-1][best_i]), float(recall[:-1][best_i])

    # max F1
    f1 = (2 * precision[:-1] * recall[:-1]) / np.clip(precision[:-1] + recall[:-1], 1e-12, None)
    best_i = int(np.nanargmax(f1))
    return float(thr[best_i]), float(precision[:-1][best_i]), float(recall[:-1][best_i])

