def build_sequences(df: pd.DataFrame, feature_cols: list, label_col: str, max_t: int):
    y_stay = df.groupby("stay_id")[label_col].max()

    X_list, y_list, stay_list = [], [], []
    df_sorted = df.sort_values(["stay_id", TIME_COL])

    for stay_id, g in df_sorted.groupby("stay_id", sort=False):
        X = np.full((max_t, len(feature_cols)), np.nan, dtype=np.float32)

        tb = g[TIME_COL].astype(int).values
        tb = np.clip(tb, 0, max_t - 1)

        vals = g[feature_cols].to_numpy(dtype=np.float32, copy=True)

        for t in np.unique(tb):
            rows = vals[tb == t]
            if rows.shape[0] == 1:
                X[t] = rows[0]
            else:
                X[t] = np.nanmean(rows, axis=0)

        X_list.append(X)
        y_list.append(float(y_stay.loc[stay_id]))
        stay_list.append(stay_id)

    X3 = np.stack(X_list, axis=0)
    y  = np.array(y_list, dtype=np.float32)
    stays = np.array(stay_list)
    return X3, y, stays

